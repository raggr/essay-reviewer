"""
Dynamic criteria generation — v0.9.0

When genre detection confidence is below threshold or the genre is classified
as "mixed", this module generates evaluation criteria tailored to the specific
text rather than relying on static genre dimension sets.

The criteria generation prompt asks the model to read the text and propose
5 evaluation dimensions specific to THIS text — what matters most for
reviewing THIS particular piece, given its form, ambitions, and methods.

Each generated criterion becomes a dimension prompt that runs in parallel,
just like static genre dimensions. The synthesis then uses a genre-agnostic
prompt designed to work with whatever criteria were generated.

Design principle: The dynamic path should produce feedback of comparable
depth to the static path. This means the criteria generation prompt must
elicit DEEP, SPECIFIC criteria — not thin category labels. Each criterion
needs enough analytical intelligence that the resulting dimension review
can be substantive.
"""

