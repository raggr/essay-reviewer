# ── Parsing and conversion ──────────────────────────────────────

def parse_criteria_response(text: str) -> Optional[List[Dict]]:
    """
    Parse criteria generation JSON response.

    Returns a list of criterion dicts, or None on failure.
    Each criterion has: name, key, description, priority.
    """
    try:
        cleaned = text.strip()
        if cleaned.startswith("```"):
            cleaned = cleaned.split("\n", 1)[1]
            if cleaned.endswith("```"):
                cleaned = cleaned[: cleaned.rfind("```")]
            cleaned = cleaned.strip()

        data = json.loads(cleaned)
        criteria = data.get("criteria", [])

        if not criteria or not isinstance(criteria, list):
            return None

        validated = []
        for i, c in enumerate(criteria):
            name = str(c.get("name", f"Criterion {i+1}"))
            key = str(c.get("key", f"dynamic_criterion_{i+1}"))
            # Sanitise key
            key = key.lower().replace(" ", "_").replace("-", "_")
            # Ensure no duplicate keys
            existing_keys = [v["key"] for v in validated]
            if key in existing_keys:
                key = f"{key}_{i+1}"

            description = str(c.get("description", ""))
            priority = int(c.get("priority", i + 1))

            if not description:
                continue

            validated.append({
                "name": name,
                "key": key,
                "description": description,
                "priority": priority,
            })

        return validated if validated else None

    except (json.JSONDecodeError, KeyError, TypeError, ValueError):
        return None


def criteria_to_dimension_prompts(criteria: List[Dict]) -> Dict:
    """
    Convert generated criteria into the standard DIMENSION_PROMPTS format
    used by the pipeline.

    Each criterion becomes a dimension prompt dict with 'name', 'priority',
    and 'prompt' keys — the same interface as static genre dimension prompts.

    Args:
        criteria: List of criterion dicts from parse_criteria_response()

    Returns:
        Dict mapping dimension keys to prompt dicts, compatible with
        the pipeline's load_genre_prompts() return format.
    """
    dimension_prompts = {}

    for criterion in criteria:
        key = criterion["key"]
        prompt_text = DYNAMIC_DIMENSION_TEMPLATE.format(
            name=criterion["name"],
            description=criterion["description"],
        )

        dimension_prompts[key] = {
            "name": criterion["name"],
            "priority": criterion["priority"],
            "prompt": prompt_text,
        }

    return dimension_prompts
